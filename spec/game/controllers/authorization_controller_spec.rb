require "spec_helper"

describe AuthorizationController do

  it "should authorize user" do
    #controller = build_controller(AuthorizationController)
    #player = build_player
    #controller.session.session.authorization?.should be(true) 
    #controller.server_authorize( { 'login' => player.login, 'password' => player.password_hash } )
    #controller.session.session.ingame?.should be(true)
    #controller.session.player.should eq(player)

  end

end